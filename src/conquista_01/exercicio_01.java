package conquista_01;

/* Declarar uma variável int numero dentro do escopo do método main
e na linha seguinte atribuir um valor para ela.
Após isso, usar o método System.out.println() para exibir o valor da variável no console.
OBS*: Se atentar ao parâmetro do método de exibição */

public class exercicio_01 {
    public static void main (String[] args) { // method - opening

        int numero; // creating local variable
        numero = 13; // assigning value
        System.out.println(numero);

    } // method - closing
}
