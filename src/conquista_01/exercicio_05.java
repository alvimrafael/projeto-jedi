package conquista_01;

public class exercicio_05 {
    public static void main(String[] args) {

        int a = 4;
        int b = 2;

        int sum = a + b;
        int minus = a - b;
        int multi = a * b;
        int div = a / b;
        int rest = a % b;

        System.out.printf("Sum = %d%n Minus = %d%n Multiplication = %d%n Division = %d%n Rest = %d%n",
                sum, minus, multi, div, rest);

    }
}
